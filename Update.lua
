local function callGemini(prompt, callback)
    local key   = getStr("gem_key")
    local model = getStr("gem_model","gemini-2.0-flash")
    local url   = "https://generativelanguage.googleapis.com/v1beta/models/" .. model .. ":generateContent?key=" .. key
    local hdr   = {["Content-Type"]="application/json"}
    local body  = cjson.encode({
        contents         = {{parts={{text=prompt}}}},
        generationConfig = {temperature=0.1, maxOutputTokens=1024}
    })
    Http.post(url, body, hdr, function(status, data)
        if status == 200 then
            local ok, d = pcall(cjson.decode, data)
            if ok and d and d.candidates and d.candidates[1] then
                local cand = d.candidates[1]
                local txt = cand.content and cand.content.parts and
                            cand.content.parts[1] and cand.content.parts[1].text
                if txt then callback(txt, nil)
                else
                    -- Check for blocked content
                    local reason = cand.finishReason or "unknown"
                    callback(nil,"Gemini: Empty response ("..reason..")")
                end
            else
                local ok2, d2 = pcall(cjson.decode, data)
                if ok2 and d2 and d2.error then
                    callback(nil,"Gemini: "..tostring(d2.error.message or "error"))
                else
                    callback(nil,"Gemini: Parse error. Try again.")
                end
            end
        elseif status == 400 then callback(nil,"Gemini: Bad request or model unavailable")
        elseif status == 403 then callback(nil,"Gemini: Invalid API key")
        elseif status == 429 then callback(nil,"Gemini: Rate limit. Please wait.")
        else callback(nil,"Gemini HTTP error: "..tostring(status)) end
    end)
end

