local function callGroq(prompt, callback)
    local key   = getStr("groq_key")
    local model = getStr("groq_model","llama-3.3-70b-versatile")
    if key == "" then
        callback(nil, "Groq: No API key. Open AI Engine Settings.")
        return
    end
    local url  = "https://api.groq.com/openai/v1/chat/completions"
    local hdrs = {
        ["Content-Type"]  = "application/json",
        ["Authorization"] = "Bearer " .. key
    }
    local bodyTbl = {
        model    = model,
        messages = {
            {role="system", content="You are a precise multilingual text assistant. Follow all instructions exactly. Return ONLY the output text. No explanations, no preamble, no markdown."},
            {role="user",   content=prompt}
        },
        max_tokens  = 1024,
        temperature = 0.1,
        stream      = false
    }
    local ok0, body = pcall(cjson.encode, bodyTbl)
    if not ok0 then callback(nil,"Groq: JSON encode error."); return end

    httpPostAsync(url, body, hdrs, function(status, data)
        if status == 200 and data and data ~= "" then
            local ok, d = pcall(cjson.decode, data)
            if ok and d and d.choices and d.choices[1] and d.choices[1].message then
                local txt = d.choices[1].message.content
                if txt and txt ~= "" then
                    callback(txt, nil)
                else
                    callback(nil,"Groq: Empty response from model.")
                end
            else
                -- Try error message
                local ok2, d2 = pcall(cjson.decode, data)
                if ok2 and d2 and d2.error then
                    callback(nil,"Groq: " .. tostring(d2.error.message or d2.error))
                else
                    callback(nil,"Groq: Parse error. Try again.")
                end
            end
        elseif status == 401 then callback(nil,"Groq: Invalid API key. Check key at console.groq.com")
        elseif status == 429 then callback(nil,"Groq: Rate limit. Wait 10 seconds and try again.")
        elseif status == 400 then callback(nil,"Groq: Bad request. Model may be unavailable, try different model.")
        elseif status == 503 then callback(nil,"Groq: Service temporarily unavailable. Try again.")
        elseif status == 0   then callback(nil,"Groq: No internet connection.")
        else callback(nil,"Groq error: HTTP " .. tostring(status)) end
    end)
end

