local function startVoiceRecognition()
    if not hasActiveApiKey() then
        service.speak("API key required. Open AI Engine Settings and add your " .. getStr("active_provider","Groq AI") .. " key.")
        return
    end
    local et = service.getEditText()
    if not et then
        service.speak("Please tap on a text field first, then activate the plugin.")
        return
    end
    -- VIBRATE on voice typing start
    vibratePhone(250)

    local langCode = getStr("selectedLangCode","ur-PK")
    local sr = SpeechRecognizer.createSpeechRecognizer(context)
    local intent = Intent(RecognizerIntent.ACTION_RECOGNIZE_SPEECH)
    intent.putExtra(RecognizerIntent.EXTRA_LANGUAGE_MODEL, RecognizerIntent.LANGUAGE_MODEL_FREE_FORM)
    intent.putExtra(RecognizerIntent.EXTRA_LANGUAGE, langCode)
    service.asyncSpeak("Listening...")
    sr.setRecognitionListener(RecognitionListener{
        onResults = function(results)
            local arr = results.getParcelableArrayList(SpeechRecognizer.RESULTS_RECOGNITION)
            if arr and arr.size() > 0 then
                local raw = arr.get(0)
                raw = applyDictionary(raw)
                raw = raw:gsub("%s+([%.,%!%?;:])","%1")
                if not raw:match("[%.!%?]%s*$") then raw = raw .. ". " end
                processWithAI(raw, function(final)
                    service.insertText(et, final)
                    service.speak(final)
                end)
            else
                service.asyncSpeak("No speech detected. Please try again.")
            end
            sr.destroy()
        end,
        onError = function(errCode)
            service.asyncSpeak("Could not understand. Please try again.")
            sr.destroy()
        end
    })
    sr.startListening(intent)
end

