local PLUGIN_PATH   = "/storage/emulated/0/\xE8\xA7\xA3\xE8\xAF\xB4/Plugins/CSR MultiTyper/main.lua"
local updateRunning = false

local function trim(s)
    return s and tostring(s):gsub("^%s*(.-)%s*$", "%1") or ""
end

local function performUpdate(code, ver)
    if not code or trim(code) == "" then
        service.speak("Update failed: empty file")
        return
    end
    updateRunning = true
    local function doIt()
        local tmp = PLUGIN_PATH .. ".tmp"
        local f = io.open(tmp, "w")
        if not f then
            updateRunning = false
            service.speak("Update failed: cannot write")
            return
        end
        f:write(code); f:close()
        pcall(os.remove, PLUGIN_PATH)
        local ok = pcall(os.rename, tmp, PLUGIN_PATH)
        updateRunning = false
        if ok then
            mainHandler.post(Runnable({run=function()
                local ud = LuaDialog(service)
                ud.setTitle("Update Successful")
                ud.setMessage("Updated to v" .. ver .. ". Restarting plugin...")
                ud.setButton("OK", function()
                    ud.dismiss()
                    mainHandler.postDelayed(Runnable({run=function()
                        local fn, err = loadfile(PLUGIN_PATH)
                        if fn then pcall(fn)
                        else service.speak("Reload error: " .. tostring(err)) end
                    end}), 1000)
                end)
                ud.show()
            end}))
        else
            pcall(os.remove, tmp)
            service.speak("Update failed: rename error")
        end
    end
    Thread(luajava.bindClass("java.lang.Runnable"){run=doIt}).start()
end

local function checkForUpdates(manual)
    if updateRunning then
        if manual then service.speak("Update already in progress") end
        return
    end
    if manual then service.speak("Checking for updates...") end
    local ts = tostring(os.time())
    Http.get(VERSION_URL .. "?t=" .. ts, function(code, resp)
        if code == 200 and resp then
            local latest = trim(resp)
            if latest ~= "" and latest ~= currentVersion then
                Http.get(UPDATE_URL .. "?t=" .. ts, function(code2, newCode)
                    if code2 == 200 and newCode and trim(newCode) ~= "" then
                        mainHandler.post(Runnable({run=function()
                            local dlg = LuaDialog(service)
                            dlg.setTitle("New Update Available!")
                            dlg.setMessage("Version " .. latest .. " is available.\nCurrent: " .. currentVersion .. "\n\nUpdate now?")
                            dlg.setButton("Update Now", function()
                                dlg.dismiss()
                                performUpdate(newCode, latest)
                            end)
                            dlg.setButton2("Later", function() dlg.dismiss() end)
                            dlg.show()
                        end}))
                    end
                end)
            else
                if manual then service.speak("Already on latest version " .. currentVersion) end
            end
        else
            if manual then service.speak("Update check failed. Check internet.") end
        end
    end)
end

