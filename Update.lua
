local dictFile    = os.getenv("EXTERNAL_STORAGE") .. "/csrmultityper_v4_dict.dat"
local changeTable = {}

local function loadDictionary()
    local f = io.open(dictFile, "r")
    if f then
        local c = f:read("*all"); f:close()
        if c and c ~= "" then
            local ok, r = pcall(cjson.decode, c)
            if ok and type(r) == "table" then changeTable = r end
        end
    end
end

local function saveDictionary()
    local f = io.open(dictFile, "w")
    if f then f:write(cjson.encode(changeTable)); f:close() end
end

local function applyDictionary(text)
    for w, c in pairs(changeTable) do
        local pat = "(%f[%a])" .. w:gsub("(%W)","%%%1") .. "(%f[%W])"
        text = text:gsub(pat, function(a,b) return a..c..b end)
        if text:lower() == w:lower() then text = c end
    end
    return text
end

