-- Pick best emoji based on sentence content
local function pickEmoji(sentence, idx)
    local i = 1
    while i <= #EMOJI_MAP do
        local pair    = EMOJI_MAP[i]
        local keyword = pair[1]
        local emoji   = pair[2]
        if sentence:find(keyword, 1, true) then
            return emoji
        end
        if sentence:lower():find(keyword:lower(), 1, true) then
            return emoji
        end
        i = i + 1
    end
    return EMOJI_DEFAULT[((idx - 1) % #EMOJI_DEFAULT) + 1]
end

-- Split text into sentences (handles English . ! ? AND Urdu stop byte D8 94)
local function splitIntoSentences(text)
    if not text or text == "" then return {} end
    local sentences = {}
    local cur = ""
    local i   = 1
    local len = #text

    while i <= len do
        local b = text:byte(i)

        if b == 0xD8 and i < len and text:byte(i+1) == 0x94 then
            -- Urdu full stop
            cur = cur .. "\xD8\x94"
            local t = cur:match("^%s*(.-)%s*$")
            if t and t ~= "" then
                sentences[#sentences + 1] = t
            end
            cur = ""
            i = i + 2

        elseif b == 0x2E or b == 0x21 or b == 0x3F then
            -- English . ! ?
            local nb = (i < len) and text:byte(i + 1) or 0
            if b == 0x2E and nb == 0x2E then
                -- Part of ... (ellipsis) - keep going
                cur = cur .. "."
                i = i + 1
            else
                cur = cur .. string.char(b)
                local t = cur:match("^%s*(.-)%s*$")
                if t and t ~= "" then
                    sentences[#sentences + 1] = t
                end
                cur = ""
                i = i + 1
            end

        elseif b >= 0xF0 then
            cur = cur .. text:sub(i, i + 3)
            i = i + 4
        elseif b >= 0xE0 then
            cur = cur .. text:sub(i, i + 2)
            i = i + 3
        elseif b >= 0xC0 then
            cur = cur .. text:sub(i, i + 1)
            i = i + 2
        else
            cur = cur .. string.char(b)
            i = i + 1
        end
    end

    -- Remaining text without ending punctuation
    local t = cur:match("^%s*(.-)%s*$")
    if t and t ~= "" then
        sentences[#sentences + 1] = t
    end

    return sentences
end

-- Add smart emoji after each sentence (Lua-side, works with ALL providers)
local function addSmartEmoji(text)
    if not text or text == "" then return text end
    local sentences = splitIntoSentences(text)
    if #sentences == 0 then return text end
    local parts = {}
    local i = 1
    while i <= #sentences do
        local emoji = pickEmoji(sentences[i], i)
        parts[#parts + 1] = sentences[i] .. " " .. emoji
        i = i + 1
    end
    return table.concat(parts, "  ")
end

-- ==================== DICTIONARY ====================
local dictFile    = os.getenv("EXTERNAL_STORAGE") .. "/csrmultityper_v4_dict.dat"
local changeTable = {}

local function loadDictionary()
    local f = io.open(dictFile, "r")
    if f then
        local c = f:read("*all")
        f:close()
        if c and c ~= "" then
            local ok, r = pcall(cjson.decode, c)
            if ok and type(r) == "table" then
                changeTable = r
            end
        end
    end
end

local function saveDictionary()
    local f = io.open(dictFile, "w")
    if f then
        f:write(cjson.encode(changeTable))
        f:close()
    end
end

local function applyDictionary(text)
    for w, c in pairs(changeTable) do
        local pat = "(%f[%a])" .. w:gsub("(%W)", "%%%1") .. "(%f[%W])"
        text = text:gsub(pat, function(a, b) return a .. c .. b end)
        if text:lower() == w:lower() then text = c end
    end
    return text
end

