-- ==================== UNIFIED PROCESSOR ====================
local function processWithAI(rawText, callback)
    local prov       = getStr("active_provider","Groq AI")
    local mode       = getStr("active_mode","auto_fix")
    local withEmoji  = getBool("emojiEnabled")
    local targetLang = getStr("targetLang","English")
    local cfg        = PROV_CFG[prov]

    if not cfg or getStr(cfg.kp) == "" then
        service.speak("No API key. Open AI Engine Settings and add your "..prov.." key.")
        callback(rawText)
        return
    end

    service.asyncSpeak("AI processing...")
    local prompt = getModePrompt(rawText, mode, targetLang)

    local function done(result, err)
        if err then
            service.speak(err)
            callback(rawText)
            return
        end
        local finalText = result or rawText
        -- Emoji is added by Lua here — works with ALL 4 providers
        if withEmoji and mode ~= "translation" then
            finalText = addSmartEmoji(finalText)
        end
        callback(finalText)
    end

    if     prov == "Groq AI"       then callGroq(prompt, done)
    elseif prov == "Google Gemini" then callGemini(prompt, done)
    elseif prov == "OpenAI"        then callOpenAI(prompt, done)
    elseif prov == "OpenRouter"    then callOpenRouter(prompt, done)
    else callback(rawText)
    end
end

