local function callGroq(prompt, callback)
    local key   = getStr("groq_key")
    local model = getStr("groq_model","llama-3.3-70b-versatile")
    local url   = "https://api.groq.com/openai/v1/chat/completions"
    local hdrs  = {
        ["Content-Type"]  = "application/json",
        ["Authorization"] = "Bearer " .. key
    }
    local body = cjson.encode({
        model    = model,
        messages = {
            {role="system",content="You are a precise multilingual text assistant. Follow all instructions exactly. Return ONLY the output text."},
            {role="user",  content=prompt}
        },
        max_tokens=1024, temperature=0.1
    })
    httpPostAsync(url, body, hdrs, function(status, data)
        if status == 200 and data then
            local ok, d = pcall(cjson.decode, data)
            if ok and d and d.choices and d.choices[1] and d.choices[1].message then
                callback(d.choices[1].message.content, nil)
            else callback(nil,"Groq: Parse error. Try again.") end
        elseif status == 401 then callback(nil,"Groq: Invalid API key")
        elseif status == 429 then callback(nil,"Groq: Rate limit. Please wait.")
        elseif status == 400 then callback(nil,"Groq: Bad request. Check model.")
        elseif status == 0   then callback(nil,"Groq: Connection failed. Check internet.")
        else callback(nil,"Groq error: "..tostring(status)) end
    end)
end

local function callGemini(prompt, callback)
    local key   = getStr("gem_key")
    local model = getStr("gem_model","gemini-2.0-flash")
    -- Key in URL for Gemini
    local url   = "https://generativelanguage.googleapis.com/v1beta/models/" .. model .. ":generateContent?key=" .. key
    local hdrs  = {["Content-Type"] = "application/json"}
    local body  = cjson.encode({
        contents         = {{parts={{text=prompt}}}},
        generationConfig = {temperature=0.1, maxOutputTokens=1024}
    })
    httpPostAsync(url, body, hdrs, function(status, data)
        if status == 200 and data then
            local ok, d = pcall(cjson.decode, data)
            if ok and d and d.candidates and d.candidates[1] then
                local cand = d.candidates[1]
                local txt  = cand.content and cand.content.parts and
                             cand.content.parts[1] and cand.content.parts[1].text
                if txt then
                    callback(txt, nil)
                else
                    local reason = (cand.finishReason or "unknown")
                    callback(nil,"Gemini: Empty response. Reason: " .. reason)
                end
            else
                local ok2, d2 = pcall(cjson.decode, data)
                if ok2 and d2 and d2.error then
                    callback(nil,"Gemini: " .. tostring(d2.error.message or d2.error))
                else
                    callback(nil,"Gemini: Parse error. Try again.")
                end
            end
        elseif status == 400 then callback(nil,"Gemini: Bad request or model unavailable")
        elseif status == 403 then callback(nil,"Gemini: Invalid API key. Check key.")
        elseif status == 429 then callback(nil,"Gemini: Rate limit. Please wait.")
        elseif status == 404 then callback(nil,"Gemini: Model not found: " .. getStr("gem_model","gemini-2.0-flash"))
        elseif status == 0   then callback(nil,"Gemini: Connection failed. Check internet.")
        else callback(nil,"Gemini error: "..tostring(status)) end
    end)
end

