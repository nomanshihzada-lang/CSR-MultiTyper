    httpPostAsync(url, body, hdrs, function(status, data)
        if status == 200 and data then
            local ok, d = pcall(cjson.decode, data)
            if ok and d and d.candidates and d.candidates[1] then
                local cand = d.candidates[1]
                local txt  = cand.content and cand.content.parts and
                             cand.content.parts[1] and cand.content.parts[1].text
                if txt then
                    callback(txt, nil)
                else
                    callback(nil,"Gemini: Empty response. Reason: " .. tostring(cand.finishReason or "unknown"))
                end
            else
                local ok2, d2 = pcall(cjson.decode, data)
                if ok2 and d2 and d2.error then
                    callback(nil,"Gemini: " .. tostring(d2.error.message or d2.error))
                else
                    callback(nil,"Gemini: Parse error.")
                end
            end
        elseif status == 400 then callback(nil,"Gemini: Bad request or model unavailable.")
        elseif status == 403 then callback(nil,"Gemini: Invalid API key.")
        elseif status == 429 then callback(nil,"Gemini: Rate limit. Please wait.")
        elseif status == 404 then callback(nil,"Gemini: Model not found: " .. model)
        elseif status == 0   then callback(nil,"Gemini: No internet connection.")
        else callback(nil,"Gemini error: HTTP " .. tostring(status)) end
    end)
end

-- ==================== UNIFIED PROCESSOR ====================
local function processWithAI(rawText, callback)
    local prov       = getStr("active_provider","Groq AI")
    local mode       = getStr("active_mode","auto_fix")
    local withEmoji  = getBool("emojiEnabled")
    local targetLang = getStr("targetLang","English")
    local cfg        = PROV_CFG[prov]

    if not cfg or getStr(cfg.kp) == "" then
        service.speak("No API key for " .. prov .. ". Open AI Engine Settings.")
        callback(rawText)
        return
    end

    service.asyncSpeak("Processing...")
    local prompt = getModePrompt(rawText, mode, targetLang)

    local function done(result, err)
        if err then
            service.speak(err)
            callback(rawText)
            return
        end
        local finalText = result or rawText
        if withEmoji then
            finalText = addSmartEmoji(finalText)
        end
        callback(finalText)
    end

    if     prov == "Groq AI"       then callGroq(prompt, done)
    elseif prov == "OpenRouter"    then callOpenRouter(prompt, done)
    elseif prov == "OpenAI"        then callOpenAI(prompt, done)
    elseif prov == "Google Gemini" then callGemini(prompt, done)
    else callback(rawText)
    end
end

