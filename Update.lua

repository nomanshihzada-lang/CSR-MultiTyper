local function httpPostAsync(url, body, hdrs, callback)
    Thread(luajava.bindClass("java.lang.Runnable"){
        run = function()
            local code = 0
            local resp = nil
            pcall(function()
                local conn = URL(url).openConnection()
                conn.setRequestMethod("POST")
                conn.setDoOutput(true)
                conn.setDoInput(true)
                conn.setUseCaches(false)
                conn.setConnectTimeout(30000)
                conn.setReadTimeout(30000)
                -- Set headers
                for k, v in pairs(hdrs) do
                    conn.setRequestProperty(k, v)
                end
                -- Write body (byte by byte = correct UTF-8 for Urdu text)
                local dos = DataOutputStream(conn.getOutputStream())
                for i = 1, #body do
                    dos.writeByte(body:byte(i))
                end
                dos.flush()
                dos.close()
                -- Get response code
                code = conn.getResponseCode()
                -- Read response body
                local stream
                if code >= 200 and code < 300 then
                    stream = conn.getInputStream()
                else
                    stream = conn.getErrorStream()
                    if stream == nil then stream = conn.getInputStream() end
                end
                if stream ~= nil then
                    local br = BufferedReader(InputStreamReader(stream, "UTF-8"))
                    local sb = StringBuilder()
                    local ln = br.readLine()
                    while ln ~= nil do
                        sb.append(ln)
                        ln = br.readLine()
                    end
                    br.close()
                    resp = sb.toString()
                end
                conn.disconnect()
            end)
            -- Post result to main thread
            mainHandler.post(Runnable({run=function()
                callback(code, resp)
            end}))
        end
    }).start()
end

