local function callGroq(prompt, callback)
    local key   = getStr("groq_key")
    local model = getStr("groq_model","llama-3.3-70b-versatile")
    local url   = "https://api.groq.com/openai/v1/chat/completions"
    local hdr   = {["Content-Type"]="application/json",["Authorization"]="Bearer "..key}
    local body  = cjson.encode({
        model    = model,
        messages = {
            {role="system",content="You are a multilingual text assistant. Follow all instructions exactly. Return only the processed output text without any extra explanation or formatting."},
            {role="user",  content=prompt}
        },
        max_tokens=1024, temperature=0.1
    })
    Http.post(url, body, hdr, function(status, data)
        if status == 200 then
            local ok, d = pcall(cjson.decode, data)
            if ok and d and d.choices and d.choices[1] and d.choices[1].message then
                callback(d.choices[1].message.content, nil)
