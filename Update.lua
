local function pickEmoji(sentence, idx)
    local style = getStr("emojiStyle","smart")
    if style ~= "smart" and STYLE_POOLS[style] then
        local pool = STYLE_POOLS[style]
        return pool[((idx-1) % #pool) + 1]
    end
    local i = 1
    while i <= #EMOJI_MAP do
        local kw = EMOJI_MAP[i][1]
        local em = EMOJI_MAP[i][2]
        if sentence:find(kw, 1, true) then return em end
        if sentence:lower():find(kw:lower(), 1, true) then return em end
        i = i + 1
    end
    return EMOJI_DEFAULT[((idx-1) % #EMOJI_DEFAULT) + 1]
end

local function splitIntoSentences(text)
    if not text or text == "" then return {} end
    local sentences = {}
    local cur = ""
    local i = 1
    local len = #text
    while i <= len do
        local b = text:byte(i)
        if b == 0xD8 and i < len and text:byte(i+1) == 0x94 then
            cur = cur .. "\xD8\x94"
            local t = cur:match("^%s*(.-)%s*$")
            if t and t ~= "" then sentences[#sentences+1] = t end
            cur = ""; i = i + 2
        elseif b == 0x2E or b == 0x21 or b == 0x3F then
            local nb = (i < len) and text:byte(i+1) or 0
            if b == 0x2E and nb == 0x2E then
                cur = cur .. "."; i = i + 1
            else
                cur = cur .. string.char(b)
                local t = cur:match("^%s*(.-)%s*$")
                if t and t ~= "" then sentences[#sentences+1] = t end
                cur = ""; i = i + 1
            end
        elseif b >= 0xF0 then cur = cur..text:sub(i,i+3); i = i+4
        elseif b >= 0xE0 then cur = cur..text:sub(i,i+2); i = i+3
        elseif b >= 0xC0 then cur = cur..text:sub(i,i+1); i = i+2
        else cur = cur..string.char(b); i = i+1
        end
    end
    local t = cur:match("^%s*(.-)%s*$")
    if t and t ~= "" then sentences[#sentences+1] = t end
    return sentences
end

local function addSmartEmoji(text)
    if not text or text == "" then return text end
    local sentences = splitIntoSentences(text)
    if #sentences == 0 then return text end
    local parts = {}
    local i = 1
    while i <= #sentences do
        parts[#parts+1] = sentences[i] .. " " .. pickEmoji(sentences[i], i)
        i = i + 1
    end
    return table.concat(parts, "  ")
end

