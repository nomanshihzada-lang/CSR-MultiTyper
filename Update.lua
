-- ==================== API CALLERS ====================

local function callGroq(prompt, callback)
    local key   = getStr("groq_key")
    local model = getStr("groq_model", "llama-3.3-70b-versatile")
    local url   = "https://api.groq.com/openai/v1/chat/completions"
    local hdr   = {["Content-Type"] = "application/json", ["Authorization"] = "Bearer " .. key}
    local body  = cjson.encode({
        model    = model,
        messages = {
            {role = "system", content = "You are a multilingual text assistant. Follow all instructions. Return only the processed output text."},
            {role = "user",   content = prompt}
        },
        max_tokens  = 1024,
        temperature = 0.1
    })
    Http.post(url, body, hdr, function(status, data)
        if status == 200 then
            local ok, d = pcall(cjson.decode, data)
            if ok and d and d.choices and d.choices[1] and d.choices[1].message then
                callback(d.choices[1].message.content, nil)
            else
                callback(nil, "Groq: Could not parse response")
            end
        elseif status == 401 then
            callback(nil, "Groq: Invalid API key")
        elseif status == 429 then
            callback(nil, "Groq: Rate limit. Please wait.")
        elseif status == 400 then
            callback(nil, "Groq: Bad request. Try different model.")
        else
            callback(nil, "Groq error: " .. tostring(status))
        end
    end)
end

local function callGemini(prompt, callback)
    local key   = getStr("gem_key")
    local model = getStr("gem_model", "gemini-2.0-flash")
    local url   = "https://generativelanguage.googleapis.com/v1beta/models/" .. model .. ":generateContent?key=" .. key
    local hdr   = {["Content-Type"] = "application/json"}
    local body  = cjson.encode({
        contents         = {{parts = {{text = prompt}}}},
        generationConfig = {temperature = 0.1, maxOutputTokens = 1024}
    })
    Http.post(url, body, hdr, function(status, data)
        if status == 200 then
            local ok, d = pcall(cjson.decode, data)
            if ok and d and d.candidates and d.candidates[1] then
                local cand = d.candidates[1]
                if cand.content and cand.content.parts and cand.content.parts[1] then
                    callback(cand.content.parts[1].text, nil)
                else
                    callback(nil, "Gemini: Empty response")
                end
            else
                callback(nil, "Gemini: Could not parse response")
            end
        elseif status == 400 then
            callback(nil, "Gemini: Bad request or model unavailable")
        elseif status == 403 then
            callback(nil, "Gemini: Invalid API key")
        elseif status == 429 then
            callback(nil, "Gemini: Rate limit. Please wait.")
        else
            callback(nil, "Gemini error: " .. tostring(status))
        end
    end)
end

