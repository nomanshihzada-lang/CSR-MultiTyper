        elseif pv == "Google Gemini" then
            testUrl  = "https://generativelanguage.googleapis.com/v1beta/models?key=" .. key
            testHdrs = nil
        elseif pv == "OpenAI" then
            testUrl  = "https://api.openai.com/v1/models"
            testHdrs = {["Authorization"] = "Bearer " .. key}
        elseif pv == "OpenRouter" then
            testUrl  = "https://openrouter.ai/api/v1/auth/key"
            testHdrs = {["Authorization"] = "Bearer " .. key}
        end

        httpGetAsync(testUrl, testHdrs, function(status, data)
            testBtn.setText("TEST API KEY"); testBtn.setEnabled(true)
            if status == 200 or status == 429 then
                service.speak("SUCCESS! " .. pv .. " API key is valid and working.")
